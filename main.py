from sklearn.ensemble import RandomForestRegressor
import numpy as np

# Data
foil = [6.6, 6.2, 5.8, 6, 6, 4, 5.9, 5.9, 4.1, 4.2, 5.85, 4.15, 4.15, 4.15]
jnd = [-1, -1, -1, 0.8, 0.8, 0.8, 1, 1, 1, 1, 0.8, 0.8, 0.8, 0.85]

# Convert the lists to numpy arrays
X = np.array(foil).reshape(-1, 1)
y = np.array(jnd)

# Create and train the Random Forest Regressor
regressor = RandomForestRegressor()
regressor.fit(X, y)

# Predict the JND for a new foil value
new_foil = 4.5  # Replace with the actual new foil value
predicted_jnd = regressor.predict([[new_foil]])

print("Predicted JND:", predicted_jnd)














































































































































